// 简体转繁体函数
function convertToTraditional(text) {
    let result = '';
    let i = 0;
    
    while (i < text.length) {
        let matched = false;
        
        // 优先匹配长词汇（最多4个字符）
        for (let len = Math.min(4, text.length - i); len >= 1; len--) {
            const substring = text.substring(i, i + len);
            
            if (simplifiedToTraditional[substring]) {
                result += simplifiedToTraditional[substring];
                i += len;
                matched = true;
                break;
            }
        }
        
        // 如果没有匹配到，保留原字符
        if (!matched) {
            result += text[i];
            i++;
        }
    }
    
    return result;
}

